package project02;

public class project04 {
	public static void main(String [] args) {
	
	int a = 5;	
	System.out.println(++a);
	System.out.println(++a);
	System.out.println(a);
	int b = 5;
	System.out.println((b++));
	System.out.println(b++);
	System.out.println(b);
	
	int c = 5;
	System.out.println((--c));
	System.out.println(--c);
	System.out.println(c);
	int d = 5;
	System.out.println(d--);
	System.out.println(d--);
	System.out.println(d);
	
	
	
	
	}
}
